/* Data Structures and Algorithms (DSA)

--------------------------------------------------

1. Data, Data Structure, and Algorithm

Data:
Data is any information or value.
Example: numbers, text, images

Data Structure:
A way of organizing and storing data
so it can be used efficiently.

Algorithm:
A step-by-step procedure
to solve a problem.

--------------------------------------------------

Connection:

Data Structures and Algorithms work together:

- Data Structure → stores data
- Algorithm → processes data

Without structure → data messy
Without algorithm → no solution

--------------------------------------------------

2. Why Learn DSA?

Efficiency:
- Handle large data efficiently

Performance:
- Reduce time (Time Complexity)
- Reduce memory usage (Space Complexity)

Problem Solving:
- Helps solve real-world problems
  like searching, routing, scheduling

--------------------------------------------------

3. Types of Data Structures

A. Primitive Data Structures:
Primitive Data Structures are basic data structures provided by programming languages to represent single values.

Basic built-in types:
- Integer (int)
- Float (decimal)
- Boolean (true/false)
- Character (char)

--------------------------------------------------

B. Abstract Data Structures:
are higher-level data structures that are built using primitive data types and provide more complex and specialized operations

Advanced structures built using primitives:
- Array
- Stack
- Queue
- Linked List
- Tree
- Graph

--------------------------------------------------

4. Classification of Data Structures

A. Linear Data Structures:

Elements are arranged in one dimension ,also known as linear dimension.

Examples:

Array:
- Fixed size
- Same data type

Linked List:
- Nodes connected using pointers
- Dynamic size

Stack:
- LIFO (Last In First Out)
- Example: Undo/Redo

Queue:
- FIFO (First In First Out)
- Example: Printer queue

--------------------------------------------------

B. Non-Linear Data Structures:

Elements are arranged in one-many, many-one and many-many dimensions.

Examples:

Tree:
- Parent-child structure
- Example: File system

Graph:
- Nodes + edges
- Example: Social networks, maps

Heap:
- Special tree for priority
- Example: Job scheduling

Hash Table:
- Key-value pairs
- Very fast searching

--------------------------------------------------

5. Common Operations

Insertion:
- Add data

Deletion:
- Remove data

Traversal:
- Visit all elements

Searching:
- Find a value

Sorting:
- Arrange data in order

Merging:
- Combine two structures

--------------------------------------------------

6. Important Concepts

Big O Notation:
- Measures algorithm performance
- Shows how time grows with input size

Recursion:
- Function calls itself
- Used to solve problems step by step

Divide and Conquer:
- Break big problem into smaller parts
- Solve and combine results

--------------------------------------------------

Conclusion:

DSA helps in writing efficient programs.
It improves speed, memory usage,
and problem-solving ability.

*/